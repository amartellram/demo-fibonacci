package com.amspace.demo.bean;

public class FibonacciNumber {

	private int number;
	private int value;
	
	
	public FibonacciNumber(int number, int value) {
		super();
		this.number = number;
		this.value = value;
	}
	
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	
	
	
	
}
